package src.beans;

public class BudgetActual extends BaseBean{
	
	private static final long serialVersionUID = 1L;
	
	private String ytmBudget;
	private String ytdActual;
	private String availBudget;
	private String estExp;
	private String expRemarks;
	
	public BudgetActual(){
		super();
	}

	public String getYtmBudget() {
		return ytmBudget;
	}

	public void setYtmBudget(String ytmBudget) {
		this.ytmBudget = ytmBudget;
	}

	public String getYtdActual() {
		return ytdActual;
	}

	public void setYtdActual(String ytdActual) {
		this.ytdActual = ytdActual;
	}

	public String getAvailBudget() {
		return availBudget;
	}

	public void setAvailBudget(String availBudget) {
		this.availBudget = availBudget;
	}

	public String getEstExp() {
		return estExp;
	}

	public void setEstExp(String estExp) {
		this.estExp = estExp;
	}

	public String getExpRemarks() {
		return expRemarks;
	}

	public void setExpRemarks(String expRemarks) {
		this.expRemarks = expRemarks;
	}

}
