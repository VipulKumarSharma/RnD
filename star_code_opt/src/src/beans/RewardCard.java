package src.beans;

public class RewardCard extends BaseBean{

	private static final long serialVersionUID = 1L;
	private String airlineName;
	private String airlineNo;
	private String hotelChainName;
	private String hotelChainNo;
	
	public RewardCard(){
		super();
	}
	
	public RewardCard(String airlineName, String airlineNo, String hotelChainName, String hotelChainNo){
		this.airlineName = airlineName;
		this.airlineNo = airlineNo;
		this.hotelChainName = hotelChainName;
		this.hotelChainNo = hotelChainNo;
	}

	public String getAirlineName() {
		return airlineName;
	}

	public void setAirlineName(String airlineName) {
		this.airlineName = airlineName;
	}

	public String getAirlineNo() {
		return airlineNo;
	}

	public void setAirlineNo(String airlineNo) {
		this.airlineNo = airlineNo;
	}

	public String getHotelChainName() {
		return hotelChainName;
	}

	public void setHotelChainName(String hotelChainName) {
		this.hotelChainName = hotelChainName;
	}

	public String getHotelChainNo() {
		return hotelChainNo;
	}

	public void setHotelChainNo(String hotelChainNo) {
		this.hotelChainNo = hotelChainNo;
	}
}
